#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*res;
	size_t	limit;

	limit = (size_t) - 1;
	if (count != 0 && limit / count < size)
		return (NULL);
	res = malloc(count * size);
	if (res == NULL)
		return (NULL);
	ft_bzero(res, count * size);
	return (res);
}

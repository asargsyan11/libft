#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*ptr;
	int		len;

	len = (int)ft_strlen(s1);
	ptr = (char *)malloc(sizeof(char) * len + 1);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
